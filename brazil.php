        <p>“Feijoada”, a black bean and pork meat stew, Brazil’s most traditional dish</p>
        <p>“Churrasco”, the Brazilian barbecue not like any other barbecue you have ever eaten, big pieces of meat are roasted with charcoal and seasoned with rock salt</p>
        <p>“Pao de queijo” (cheese bread), a delicious snack eaten in breakfast or in the afternoon made with cassava starch and cheese</p>
        <p>“Brigadeiro”, most famous dessert made with condensed milk and chocolate.</p>
        <p>"Caipirinha", Brazil's traditional drink, the original one is made with lime and "cachaça" (sugar cane liquor) having variations made with other fruits and liquor</p>

